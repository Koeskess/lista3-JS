var A=parseFloat(prompt("Informe o valor A: "));
var B=parseFloat(prompt("Informe o valor B: "));
var C=parseFloat(prompt("Informe o valor C: "));

var A2= A * A;
var B2= B * B;
var C2= C * C;
var soma= A2 + B2 + C2;

document.write("A soma dos quadrados dos três valores é igual a: " + soma);